#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (i += 6);
	}
	while (s[i])
	{
		i += ft_putchar(s[i]);
	}
	return (i);
}
