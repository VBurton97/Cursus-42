int	ft_is_prime(int nb)
{
	int	i;

	if (nb == 1 || nb == 0 || nb < 0)
		return (0);
	i = 2;
	while (i < 46341 && i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
