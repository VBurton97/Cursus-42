#include<unistd.h>

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	if (n <= 2147483647 && n > -2147483648)
	{
		if (n < 10 && n >= 0)
		{
			n = n + 48;
			write(1, &n, 1);
		}
		else if (n < 0)
		{
			write(1, "-", 1);
			ft_putnbr(n * -1);
		}
		else
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
	}
}
